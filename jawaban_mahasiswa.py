def tambah(a, b):
    """
    Fungsi untuk menjumlahkan a dan b.
    """
    # Tulis kode Anda di bawah ini
    return a + b

def kurang(a, b):
    """
    Fungsi untuk mengurangkan a dengan b.
    """
    # Tulis kode Anda di bawah ini
    return a - b

def kali(a, b):
    """
    Fungsi untuk mengalikan a dan b.
    """
    # Tulis kode Anda di bawah ini
    return a * b

def bagi(a, b):
    """
    Fungsi untuk membagi a dengan b.
    """
    # Tulis kode Anda di bawah ini
    return a / b
